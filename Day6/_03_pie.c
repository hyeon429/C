#include <stdio.h>

int main3() {
	// 1byte == 8bit : 256가지의 경우의 수(2^8)
	// 1bit : 2가지의 경우의 수 (2^1) == 0 or 1
	// 2bit : 4가지의 경우의 수 (2^2) == 00 or 01 or 10 or 11 (0,1,2,3)

	char c;  	// 0~255 (영어 26가지, 한글 256가지를 넘어감)
	short s;	// 2byte (2^16)가지 ==> 작은 정수
	int j;	    // 4byte			==> 기본 정수
	float f;	// 4byte			==> 작은 실수
	double d;	// 8byte			==> 큰 실수





	return 0;


}